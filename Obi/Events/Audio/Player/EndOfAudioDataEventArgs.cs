using System;

namespace Obi.Events.Audio.Player
{
	public delegate void EndOfAudioAssetHandler(object sender, EndOfAudioAssetEventArgs e);
	public delegate void EndOfAudioBufferHandler(object sender, EndOfAudioBufferEventArgs e);

	/// <summary>
	/// The end of the audio data currently playing has been reached.
    /// This is subclassed to include both the end of a buffer as well as the end of an asset.
	/// </summary>
	public abstract class EndOfAudioDataEventArgs: EventArgs
	{
	}

	public class EndOfAudioAssetEventArgs: EndOfAudioDataEventArgs
	{
	}

	public class EndOfAudioBufferEventArgs: EndOfAudioDataEventArgs
	{
	}
}